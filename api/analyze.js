export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  try {
    const { topTalents, allScores, confirmedPatterns, deniedPatterns } = req.body;

    const topList = topTalents
      .map((t, i) => `${i + 1}. ${t.name} — ${t.desc}`)
      .join('\n');

    const allList = Object.entries(allScores)
      .sort((a, b) => b[1] - a[1])
      .map(([t, s]) => `${t}: ${s}`)
      .join(', ');

    const confirmedList = (confirmedPatterns || [])
      .map(p => `- "${p}"`)
      .join('\n') || '(не передано)';

    const deniedList = (deniedPatterns || [])
      .map(p => `- "${p}"`)
      .join('\n') || '(не передано)';

    const prompt = `Ти — коуч з розвитку особистості. Твій стиль: точний, образний, без кліше і порожніх фраз. Ти кажеш те, що людина відчуває але не може сформулювати сама.

ТОП-5 ТАЛАНТІВ:
${topList}

ПОВНИЙ ПРОФІЛЬ (всі таланти за балами):
${allList}

ПІДТВЕРДЖЕНІ ПАТЕРНИ — твердження з якими людина погодилась (оцінка 4-5):
${confirmedList}

ЗАПЕРЕЧЕНІ ПАТЕРНИ — твердження які людина відкинула (оцінка 1-2):
${deniedList}

Напиши глибокий персональний аналіз УКРАЇНСЬКОЮ МОВОЮ.

Жорсткі правила тону:
- Жодних фраз: "ти людина яка...", "твоя природа...", "ти народжений щоб...", "твоя суперсила — це..."
- Жодних загальних слів про "лідерство", "успіх", "натхнення" без конкретики
- Кожне твердження має спиратись на конкретну комбінацію талантів або підтверджені патерни
- Говори з людиною як рівний з рівним, не зверху вниз

### Твоя суть
Одна влучна метафора або образ (не кліше) + 2 речення що КОНКРЕТНО відрізняє цю людину від більшості. Використай підтверджені та заперечені патерни щоб зробити це точним.

### Як твої топ-5 працюють разом
Опиши один конкретний реальний сценарій (робочий або життєвий) де всі або більшість з 5 талантів включаються одночасно. Покажи механіку — що відбувається всередині, чому ця людина діє саме так. Потім коротко — як ці таланти підсилюють один одного.

### Твої суперсили
3 конкретні ситуації де ця людина природно випереджає більшість людей. Формат: "Коли [конкретна ситуація] — ти [що робиш інакше] тому що [механіка талантів]." Без абстракцій.

### Сліпі зони
2 патерни що виникають саме з ЦІЄЇ комбінації талантів — не загальні мінуси, а специфічні пастки. Формат: ситуація → що відбувається → чому це логічно з твоїх талантів → що з цим робити.

### Кар'єра і роль
5 конкретних ролей або позицій (не просто "сфер") з поясненням чому саме вони розкривають цей профіль. Можна додати 1-2 ролі яких варто уникати — і чому.

### Для команди
З якими типами людей ця комбінація створює синергію і чому (через механіку талантів). З якими виникає тертя і як це проявляється. Конкретно, не "з творчими людьми" а через що саме.

### Три кроки цього тижня
Три малі але точні дії. Формат кожної: ЩО зробити (одна конкретна дія, не "більше X") + КОЛИ або в якій ситуації + як зрозуміти що вийшло. Дії мають бути такими щоб людина могла почати завтра.`;

    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01',
      },
      body: JSON.stringify({
        model: 'claude-sonnet-4-5',
        max_tokens: 3000,
        messages: [{ role: 'user', content: prompt }],
      }),
    });

    const data = await response.json();
    const text = data.content?.[0]?.text || 'Не вдалось отримати аналіз.';
    res.status(200).json({ analysis: text });

  } catch (err) {
    console.error(err);
    res.status(500).json({ error: 'Internal server error' });
  }
}
